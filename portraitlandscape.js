window.onresize = function (event) {
  applyOrientation();
}

function applyOrientation() {
  if (window.innerHeight < window.innerWidth) {
   alert("You are now in 1111");
  } else {
    if (window.innerHeight < window.innerWidth) {
   alert("You are now in 2222");
  } else {
    alert("You are now in 33333");
  }
}}